## Get installed programs list with date installed

$wshell = New-Object -ComObject Wscript.Shell -ErrorAction Stop
$check = '0'
[System.Reflection.Assembly]::LoadWithPartialName('Microsoft.VisualBasic') | Out-Null
$RemotePC = [Microsoft.VisualBasic.Interaction]::InputBox("Enter Remote Computer Name ","Get Installed Programs")
$creds = Get-Credential
$SB = {$check = Get-CimInstance -ClassName Win32_Product -ErrorAction SilentlyContinue | Select-Object Name, InstallDate; return $check}
$x = New-PSSession $RemotePC
Invoke-Command -Session $x -ScriptBlock $SB -Credential $creds | Out-GridView
Remove-PSSession $x



